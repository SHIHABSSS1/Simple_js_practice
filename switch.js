var num = 5;
switch (num) {
  case 1:
    console.log("Number is 1");
    break;
    case 2:
    console.log("Number is 2");
    break;
    case 3:
    console.log("Number is 3");
    break;
    case 4:
    console.log("Number is 4");
    break;
    case 5: 
    console.log("Number is 5");
    break;
    default:
    console.log("Number is not between 1 and 5"); 
}