//and = &&
var experience = true;
var age = 22;

if (experience = true && age < 18) {
    console.log("(and&&)You are eligible for the job.");
}
else {
    console.log("(and&&)You are not eligible for the job.");
}

//or ||
var experience = true;
var age = 22;

if (experience = true || age < 18) {
    console.log("(or)You are eligible for the job.");
}
else {
    console.log("(or)You are not eligible for the job.");
}
